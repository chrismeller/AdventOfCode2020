        public static List<List<string>> ParsedInput
        {
            get
            {
                var parsed = new List<List<string>>();
                var lines = Input.Trim().Split("\r\n", StringSplitOptions.None);
                foreach(var line in lines)
                {
                    var parsedLine = line.Trim().ToCharArray().Select(x => x.ToString()).ToList();
                    parsed.Add(parsedLine);
                }

                return parsed;
            }
        }

        public static int Solve()
        {
            var stepX = 3;
            var stepY = 1;

            var trees = 0;

            var x = 0;
            var y = 0;
            do
            {
                Console.Out.Write($"x: {x}, y: {y}");

                if (IsTree(x, y))
                {
                    Console.Out.Write("\tTree!");
                    trees++;
                }

                x = x + stepX;
                y = y + stepY;

                Console.Out.WriteLine();
            } 
            while (IsEnd(x, y) == false);

            return trees;

        }

        public static bool IsEnd(int x, int y)
        {
            if (y > ParsedInput.Count - 1)
            {
                return true;
            }

            return false;
        }

        public static bool IsTree(int x, int y)
        {
            var line = ParsedInput[y];

            var realPos = FindRealLinePos(line, x);
            var pos = FindLinePos(line, x);

            Console.Out.Write($"\tpos: {realPos}");
            Console.Out.Write("\t" + String.Join(null, line));

            if (pos == "#")
            {
                return true;
            }

            return false;
        }

        /// <summary>
        /// Since the lines repeat infinitely, this helper method handles that repetition.
        /// </summary>
        /// <param name="line"></param>
        /// <param name="pos"></param>
        /// <returns></returns>
        public static string FindLinePos(List<string> line, int pos)
        {
            var realPos = FindRealLinePos(line, pos);

            return line[realPos];
        }

        public static int FindRealLinePos(List<string> line, int pos)
        {
            var realPos = pos;
            while (realPos > line.Count - 1)
            {
                //Console.Out.Write($"\t{realPos} > {line.Count - 1}");
                realPos = realPos - (line.Count);
                //Console.Out.WriteLine($"; realPos = {realPos}");
            }

            return realPos;
        }
